public class Barrier {
	
   Position p;
   public double chang;
   public double kuan;
   public double gao;
   
    public Barrier(double x,double y,double z,double chang,double kuan, double gao) 
    {
    	this.p.setX(x);
    	this.p.setY(y);
    	this.p.setZ(z);
    	this.chang=chang;
    	this.kuan=kuan;
    	this.gao=gao;
    }

	
}
